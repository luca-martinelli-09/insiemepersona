    <h2 class="title">Informativa sull'uso dei <span class="green">cookie</span></h2>
    <div class="text-xl mt-10" />
    <p>
      Il sito web Viviamo Sona utilizza non utilizza cookie di profilazione, ma si avvale di cookie tecnici e tecnologie simili strettamente necessari al suo funzionamento. Il presente documento
      fornisce informazioni dettagliate sull'uso dei cookie e di tecnologie similari e su come gestirli.
    </p>

    <h3 class="title">Definizioni</h3>
    <p>Il Titolare del Trattamento dei dati personali relativi alla navigazione all'interno del presente sito web è Viviamo Sona.</p>

    <h3 class="title">Tipologie di Dati Raccolti</h3>
    <p>
      I cookie sono brevi frammenti di testo (lettere e/o numeri) che permettono al server web di memorizzare sul client (il browser) informazioni da riutilizzare nel corso della medesima visita al
      sito (cookie di sessione) o in seguito, anche a distanza di giorni (cookie persistenti). I cookie vengono memorizzati, in base alle preferenze dell'utente, dal singolo browser sullo specifico
      dispositivo utilizzato (computer, tablet, smartphone).
    </p>
    <p>
      Tecnologie similari, come, ad esempio, web beacon, GIF trasparenti e tutte le forme di storage locale introdotte con HTML5, sono utilizzabili per raccogliere informazioni sul comportamento
      dell'utente e sull'utilizzo dei servizi.
    </p>
    <p>Nel seguito di questo documento faremo riferimento ai cookie e a tutte le tecnologie similari utilizzando semplicemente il termine "cookie".</p>

    <h3 class="title">Tipologie di cookie</h3>
    <p>In base alle caratteristiche e all'utilizzo dei cookie possiamo distinguere diverse categorie:</p>

    <ul class="list-disc list-inside">
      <li>
        <strong>Cookie strettamente necessari</strong>. Si tratta di cookie indispensabili per il corretto funzionamento deel sito e sono utilizzati per gestire il login e l'accesso alle funzioni
        riservate del sito. La durata dei cookie è strettamente limitata alla sessione di lavoro (chiuso il browser vengono cancellati);
      </li>
      <li>
        <strong>Cookie di analisi e prestazioni</strong>. Sono cookie utilizzati per raccogliere e analizzare il traffico e l'utilizzo del sito in modo anonimo. Questi cookie, pur senza identificare
        l'utente, consentono, per esempio, di rilevare se il medesimo utente torna a collegarsi in momenti diversi. Permettono inoltre di monitorare il sistema e migliorarne le prestazioni e
        l'usabilità. La disattivazione di tali cookie può essere eseguita senza alcuna perdita di funzionalità;
      </li>
      <li>
        <strong>Cookie di profilazione</strong>. Si tratta di cookie permanenti utilizzati per identificare (in modo anonimo e non) le preferenze dell'utente e migliorare la sua esperienza di
        navigazione. <strong>Questo sito non utilizza cookie di questo tipo</strong>.
      </li>
    </ul>

    <h3 class="title">Cookie di terze parti</h3>
    <p>
      Visitando un sito web si possono ricevere cookie sia dal sito visitato ("proprietari"), sia da siti gestiti da altre organizzazioni ("terze parti"). Un esempio notevole è rappresentato dalla
      presenza dei "social plugin" per Facebook, Twitter, Google+ e LinkedIn. Si tratta di parti della pagina visitata generate direttamente dai suddetti siti ed integrati nella pagina del sito
      ospitante. <strong>Questo sito non utilizza cookie di questo tipo</strong>.
    </p>

    <h3 class="title">Durata dei cookie</h3>
    <p>
      Alcuni cookie (cookie di sessione) restano attivi solo fino alla chiusura del browser o all'esecuzione del comando di logout. Altri cookie "sopravvivono" alla chiusura del browser e sono
      disponibili anche in successive visite dell'utente.
    </p>
    <p>Questi cookie sono detti persistenti e la loro durata è fissata dal server al momento della loro creazione. In alcuni casi è fissata una scadenza, in altri casi la durata è illimitata.</p>
    <p>
      <strong>Il sito Viviamo Sona non fa uso di cookie persistenti.</strong>
    </p>

    <h3 class="title">Gestione dei cookie</h3>
    <p>L'utente può decidere se accettare o meno i cookie utilizzando le impostazioni del proprio browser.</p>
    <p>La disabilitazione dei cookie "terze parti" non pregiudica in alcun modo la navigabilità.</p>
    <p>
      L'impostazione può essere definita in modo specifico per i diversi siti e applicazioni web. Inoltre i migliori browser consentono di definire impostazioni diverse per i cookie "proprietari" e
      per quelli di "terze parti".
    </p>
